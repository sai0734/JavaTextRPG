    // [스토리용]
    public static void playStory(String[] story) {
        for (int i = 0; i < story.length; i++) {
            final String line = story[i];
            Timer t = new Timer(200 * i, new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    GameFrame.logArea.append(line + "\n");
                }
            });
            t.setRepeats(false);
            t.start();
        }
    }
    
    // [시작용]
    public static void fastPlayStory(String[] story) {
        for (String line : story) {
            GameFrame.logArea.append(line + "\n");
        }
    }
    
